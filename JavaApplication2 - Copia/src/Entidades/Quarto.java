public class Quarto {

    private int numero;
    private int andar;
    private int cod_categoria;
    private int iCod;

    public int getTxtNumero() {
        return numero;
    }

    public void setTxtNumero(int TxtNumero) {
        this.numero = TxtNumero;
    }

    public int getTxtAndar() {
        return andar;
    }

    public void setTxtAndar(int TxtAndar) {
        this.andar = TxtAndar;
    }

    public int getTxtCodCategoria() {
        return cod_categoria;
    }

    public void setTxtCodCategoria(int TxtCodCategoria) {
        this.cod_categoria = TxtCodCategoria;
    }

    public int getiCod() {
        return iCod;
    }

    public void setiCod(int iCod) {
        this.iCod = iCod;
    }
    


}



    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Botão Pesquisar
        // cria lista cidade e alimenta com consulta do banco de dados
        List<Produto> list_Pd = Pd.consulta_produto(jTextField4.getText());
        //Capturar o modelo padrao da nossa tabela
        DefaultTableModel mod_tab = (DefaultTableModel) jTable1.getModel();
        mod_tab.getDataVector().removeAllElements();
        for (int i = 0; i <= list_Pd.size() - 1; i++) {
            mod_tab.addRow(new Object[]{
                list_Pd.get(i).getCod_produto(),
                list_Pd.get(i).getDescricao(),
                list_Pd.get(i).getValor_unitario(),
                list_Pd.get(i).getStats()});
        }
    }//GEN-LAST:event_jButton4ActionPerformed


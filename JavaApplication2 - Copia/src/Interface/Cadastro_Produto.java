    private void StatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StatusActionPerformed
        // TODO add your handling code here:
        Produto_DAO Pd = new Produto_DAO();
        Produto st = new Produto();
        st.setValor_unitario(Double.parseDouble(valor_unitario.getText()));
        st.setStats(stats.getText());
        st.setDescricao(descricao.getText());
        st.setCod_produto(Integer.parseInt(codigo.getText()));
        if (st.getStats().equals("On")) {
            st.setStats("Off");
            if (Pd.alterar(st)) {
                JOptionPane.showMessageDialog(null, "Desabilitado com sucesso");
                limpar();
            jButton4ActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar");
            }
        } else {
            st.setStats("On");
            if (Pd.alterar(st)) {
                JOptionPane.showMessageDialog(null, "Habilitado com sucesso");
                limpar();
            jButton4ActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar");
            }
        }

    }//GEN-LAST:event_StatusActionPerformed


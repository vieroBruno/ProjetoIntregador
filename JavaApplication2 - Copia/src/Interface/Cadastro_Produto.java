    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // botão inserir

        Produto p = new Produto();
        //alimetna o objeto Produto com as informações da interface
        p.setValor_unitario(Double.parseDouble(valor_unitario.getText()));
        p.setDescricao(descricao.getText());
        p.setStats("On");

        //tentamos realiar a inserçãono banco em caso de falha mostra a menagem do else
        if (Pd.inserir(p)) {
            JOptionPane.showMessageDialog(rootPane, "Sucesso");
            Pd.desconectar();
            limpar();
            jButton4ActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falha");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


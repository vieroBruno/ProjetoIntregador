    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
// evento do mouse na tabela
        Produto_DAO Pd = new Produto_DAO();
        // pega a linha selecionada
        int linha = jTable1.getSelectedRow();
        //pega a coluna 0
        int coluna = 0;
        //pega o valor da tabela e passa para a variavel cod
        Object cod = jTable1.getValueAt(linha, coluna);
        if (cod != null) {
            //cria uma lista do objeto produto
            List<Produto> listp = Pd.lista_produto(Integer.parseInt(cod.toString()));//Implementar metodo de consulta
            //for para percorrer a lista e alimentar alguma coisa
            for (int i = 0; i < listp.size(); i++) {
                //Implementar
                descricao.setText(listp.get(i).getDescricao());
                valor_unitario.setText(String.valueOf(listp.get(i).getValor_unitario()));
                codigo.setText(String.valueOf(listp.get(i).getCod_produto()));
                stats.setText(String.valueOf(listp.get(i).getStats()));
            }
        }
        jButton3.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked


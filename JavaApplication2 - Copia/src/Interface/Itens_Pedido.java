    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        // evento do mouse na tabela
        // pega a linha selecionada
        int linha = jTable2.getSelectedRow();
        //pega a coluna 0
        int coluna = 0;
        //pega o valor da tabela e passa para a variavel cod
        Object cod = jTable2.getValueAt(linha, coluna);
        // JOptionPane.showMessageDialog(rootPane, Tabela.getValueAt(linha, coluna));
        if (cod != null) {

            Itens_DAO itensDao = new Itens_DAO();
            Itens itemInse = new Itens();
            //cria uma lista do objeto Dependentes

            //Implementar metodo de consulta
            List<Itens> list_Itens = itensDao.consulta_itens(Integer.parseInt(cod.toString()), cod_Pedido);

            //for para percorrer a lista e alimentar alguma coisa
            for (int i = 0; i < list_Itens.size(); i++) {
                //Implementar
                jTextField1.setText(String.valueOf(list_Itens.get(i).getCod()));
                // Coloca a posição combox 
                jComboBox1.setSelectedItem(
                        String.valueOf(list_Itens.get(i).getDescricao()));
                jTextField4.setText(String.valueOf(list_Itens.get(i).getQuantidade()));

            }
        }
    }//GEN-LAST:event_jTable2MouseClicked


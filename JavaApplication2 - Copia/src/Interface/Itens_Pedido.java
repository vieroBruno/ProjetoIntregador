    //carregar os produtos
    public void carrega_produtos(String prod) {
        Produto_DAO pd = new Produto_DAO();
        //armazenar produtos
        List<Produto> list_Prod = pd.consulta_produto(prod);

        for (int i = 0; i <= list_Prod.size() - 1; i++) {
            jComboBox1.addItem(list_Prod.get(i).getDescricao());
            jTextField3.setText(String.valueOf(list_Prod.get(i).getCod_produto()));
        }
    }

    public void carrega_itens(int cod) {

        Itens_DAO Hd = new Itens_DAO();
        //cria lista cidade e alimenta com a consulta do banco de dados
        List<Itens> list_item = Hd.consulta_itens(cod);

        //Capturar o modelo poadrao da nossa tabela
        DefaultTableModel mod_tab = (DefaultTableModel) jTable2.getModel();
        mod_tab.getDataVector().removeAllElements();
        for (int i = 0; i <= list_item.size() - 1; i++) {
            //adciona as linhas com o retorno do banco de dados
            mod_tab.addRow(new Object[]{
                list_item.get(i).getCod(),
                list_item.get(i).getDescricao(),
                list_item.get(i).getQuantidade(),
                list_item.get(i).getVlr()

            });
        }

    }


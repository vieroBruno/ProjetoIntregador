    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Quarto_DAO Q_dao = new Quarto_DAO();
        List<Quarto> lista_quarto = Q_dao.consulta_quarto(jTextField1.getText());

        for (int i = 0; i <= lista_quarto.size() - 1; i++) {
            System.out.println("Codigo do hospede " + lista_quarto.get(i).getiCod());
        }

        //Capturar o modelo padrao da nossa tabela
        DefaultTableModel mod_tab = (DefaultTableModel) jTable1.getModel();
        mod_tab.getDataVector().removeAllElements();
        for (int i = 0; i <= lista_quarto.size() - 1; i++) {

            mod_tab.addRow(new Object[]{
                lista_quarto.get(i).getiCod(),
                lista_quarto.get(i).getTxtNumero(),
                lista_quarto.get(i).getTxtAndar()});
        }

    }//GEN-LAST:event_jButton1ActionPerformed


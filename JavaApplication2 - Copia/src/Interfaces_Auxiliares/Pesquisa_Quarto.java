    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione um quarto");
        } else {
            cod_quarto = Integer.parseInt(jTextField3.getText());

            if (origem.equals("Reserva")) {
                Reserva_Interface c = new Reserva_Interface(cod, cod_quarto);
                c.setVisible(true);
                this.dispose();
            }
            if (origem.equals("Chek")) {
                Check_Interface c = new Check_Interface(cod, cod_quarto);
                c.setVisible(true);
                this.dispose();
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

